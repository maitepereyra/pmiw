function estructura (figuras , posX , posY)  {
  let lado = width / 2 / figuras;
  for (let x = 0; x < figuras; x++) {
    for (let y = 0; y < figuras; y++) {
      push();
      translate(lado / 2, lado / 2);
      if (colores) {
        fill(random(255), random(255), random(255));
      } else {
        fill(pintar(x, y));
      }
      noStroke();
      if ((x + y) % 2 != 0) {
        rect(posX + x * lado, posY + y * lado, lado, lado);
      } else {
        circle(posX + x * lado, posY + y * lado, lado);
      }
      pop();
    }
  }
  }
  

